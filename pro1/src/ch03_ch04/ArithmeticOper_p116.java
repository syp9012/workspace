package ch03_ch04;

// 산술연산자 + - * /(몫) %(나머지)
public class ArithmeticOper_p116 {

	public static void main(String[] args) {
		double n1 = 10.0;
		double n2 = 3.0;
		
		
		double result = n1 / n2;
		System.out.println(result);  // 3
		double result1 = n1 % n2;
		System.out.println(result1);  // 1
		
	}

}
